import hasOwnProperty from './hasOwnProperty';
import is from './objectIs';

/**
 * Performs equality by iterating through keys on an object and returning false
 * when any key has values which are not strictly equal between the arguments.
 * Returns true when the values of all keys are strictly equal.
 */
function shallowEqual(objA: unknown, objB: unknown): boolean {
  if (is(objA, objB)) {
    return true;
  }

  if (
    typeof objA !== 'object' ||
    objA === null ||
    typeof objB !== 'object' ||
    objB === null
  ) {
    return false;
  }

  const keysA = Object.keys(objA);
  const keysB = Object.keys(objB);

  if (keysA.length !== keysB.length) {
    return false;
  }

  // Test for A's keys different from B.
  for (let i = 0; i < keysA.length; i++) {
    const currentKey = keysA[i];
    if (
      !hasOwnProperty.call(objB, currentKey as string) ||
      !is(
        (objA as Record<string, unknown>)[currentKey as string],
        (objB as Record<string, unknown>)[currentKey as string],
      )
    ) {
      return false;
    }
  }

  return true;
}

export default shallowEqual;
